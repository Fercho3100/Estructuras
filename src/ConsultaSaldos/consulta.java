    private void i_operacionesItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_i_operacionesItemStateChanged
        // TODO add your handling code here:
        if(evt.getStateChange() == ItemEvent.SELECTED){
            DefaultTableModel model = (DefaultTableModel) tabledata.getModel();
            model.setRowCount(0);
            ResultSet resultado = null;
            Object item = i_operaciones.getSelectedItem();
            String value = ((ComboItem)item).getValue();
            
            resultado = datosp.MovOperaciones(Integer.parseInt(value));
             try {
                while(resultado.next()){
                    // Lista
                    System.out.println(resultado.getObject(3));
                    
                    String[] info = new String[8];
                    
                    info[0] = "" + resultado.getObject(7);
                    info[1] = ""+resultado.getObject(4);
                    info[2] = ""+resultado.getObject(5);
                    info[3] = "" + resultado.getObject(6);

                    modelo.addRow(info);
                         
                }
            } catch (SQLException ex) {
                System.out.println(ex);
                Logger.getLogger(consulta.class.getName()).log(Level.SEVERE, null, ex);
            }
              
        }
        
        
        
    }//GEN-LAST:event_i_operacionesItemStateChanged

