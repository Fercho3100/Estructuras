    private void c_asociadosItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_c_asociadosItemStateChanged
        i_operaciones.removeAllItems();
        decimalFormat.setGroupingUsed(true);
        decimalFormat.setGroupingSize(3);
        if(evt.getStateChange() == ItemEvent.SELECTED)
        {

            Object item = c_asociados.getSelectedItem();
            String value = ((ComboItem)item).getValue();
            System.out.println("------");
            System.out.println(value);
          
            
            ResultSet resultado = null;
            resultado = datosp.Operaciones(Integer.parseInt(value));
            i_operaciones.addItem(new ComboItem( "Seleccione un Crédito", "-1"));
            System.out.println(resultado);
            try {
                while(resultado.next()){
                    // Lista
                    System.out.println(resultado.getObject(3));
                    
                    i_operaciones.addItem(new ComboItem("₡ "+decimalFormat.format(resultado.getObject(3)), ""+resultado.getObject(1)));
                         
                }
            } catch (SQLException ex) {
                System.out.println(ex);
                Logger.getLogger(consulta.class.getName()).log(Level.SEVERE, null, ex);
            }
             
        }
        
    }//GEN-LAST:event_c_asociadosItemStateChanged

